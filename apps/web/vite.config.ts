import { defineConfig } from 'vite';
import react from '@vitejs/plugin-react';
import tailwindcss from '@tailwindcss/vite';
import path from 'path';
import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'fs';

const apiGeneratedDir = path.resolve(__dirname, '../../apps/api/src/generated');
const apiSpaShellPath = path.resolve(apiGeneratedDir, 'spa-shell.ts');

export default defineConfig({
  plugins: [
    react(),
    tailwindcss(),
    {
      name: 'spa-shell-embed',
      closeBundle() {
        const html: string = readFileSync(path.resolve(__dirname, 'dist/index.html'), 'utf-8');
        const output: string = `// Auto-generated by Vite build plugin — DO NOT EDIT.\nexport const SPA_HTML: string = ${JSON.stringify(html)};`;
        if (!existsSync(apiGeneratedDir)) {
          mkdirSync(apiGeneratedDir, { recursive: true });
        }
        writeFileSync(apiSpaShellPath, output);
      },
    },
  ],
  server: {
    proxy: {
      '/api': {
        target: 'http://localhost:8787',
        changeOrigin: true,
      },
    },
  },
  build: {
    outDir: 'dist',
    emptyOutDir: true,
    minify: 'terser',
    terserOptions: {
      compress: {
        drop_console: true,
        drop_debugger: true,
      },
    },
  },
});